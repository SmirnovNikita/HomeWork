package math;
import java.util.Scanner;
public class HomeWorkTask2 {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		double sideA;
		System.out.println("Введите сторону треугольника А");
			sideA=sc.nextDouble();
	double sideB;
		System.out.println("Введите сторону треугольника B");
			sideB=sc.nextDouble();
	double sideC;
		System.out.println("Введите сторону трегольника C");
			sideC=sc.nextDouble();
	double semiPerimeter=(sideA+sideB+sideC)/2;
	double area=Math.sqrt(semiPerimeter*(semiPerimeter-sideA)*(semiPerimeter-sideB)*(semiPerimeter-sideC));
	System.out.println("Полупериметр треугольника="+semiPerimeter);
	System.out.println("Площадь треугольника="+area);
	sc.close();
	}

}
